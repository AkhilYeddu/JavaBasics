public class variables {
    //variables= acts as a placeholder for a value and behaves as the value it contains.
    public static void main(String[] args) {
        int x=10;
        double y=5.25;
        boolean z=true;
        char symbol='a';
        String name="akhil";
        System.out.println("hello "+name);
        System.out.println(x+" is an integer");
        System.out.println(y+" is a floating number");
        System.out.println(z+" is a boolean value");
        System.out.println(symbol+" is a character");

    }
    
}
